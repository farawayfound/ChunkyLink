# ── Helpers ───────────────────────────────────────────────────────────────────

def run(cmd, **kwargs):
    print(f"  $ {' '.join(str(c) for c in cmd)}")
    result = subprocess.run(cmd, **kwargs)
    if result.returncode != 0:
        sys.exit(f"Command failed (exit {result.returncode})")
    return result


def find_tesseract():
    candidates = [
        Path(os.environ.get("PROGRAMFILES", "C:/Program Files")) / "Tesseract-OCR" / "tesseract.exe",
        Path(os.environ.get("LOCALAPPDATA", "")) / "Programs" / "Tesseract-OCR" / "tesseract.exe",
        Path(os.environ.get("APPDATA", "")) / "Tesseract-OCR" / "tesseract.exe",
    ]
    for p in candidates:
        if p.exists():
            return str(p.parent)
    return str(candidates[1].parent)  # return default even if not found


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    script_dir = Path(__file__).resolve().parent
    default_repo = script_dir.parent.parent  # Setup/Local/ -> Setup/ -> repo root

    p = argparse.ArgumentParser(description="Set up vpoRAG local environment")
    p.add_argument("--repo-root",    default=str(default_repo))
    p.add_argument("--src-dir",      default="")
    p.add_argument("--tesseract",    default="")
    p.add_argument("--search-mode",  default="Local", choices=["Local", "MCP"])
    p.add_argument("--jira-source",  default="csv",   choices=["csv", "mysql", "sql"])
    p.add_argument("--python",       default="python")
    p.add_argument("--skip-venv",    action="store_true")
    p.add_argument("--skip-spacy",   action="store_true")
    p.add_argument("--skip-config",  action="store_true")
    p.add_argument("--force-config", action="store_true")
    args = p.parse_args()

    repo   = Path(args.repo_root).resolve()
    venv   = repo / ".venv"
    pip    = venv / "Scripts" / "pip.exe"
    python = venv / "Scripts" / "python.exe"

    print(f"\n=== vpoRAG Local Setup ===")
    print(f"Repo root : {repo}")
    print(f"Venv      : {venv}\n")

    # ── Step 1: venv + dependencies ──────────────────────────────────────────
    if not args.skip_venv:
        if not (venv / "Scripts" / "activate").exists():
            print("[1/3] Creating virtual environment...")
            run([args.python, "-m", "venv", str(venv)])
        else:
            print("[1/3] Virtual environment already exists — skipping creation.")

        print("      Upgrading pip...")
        run([str(python), "-m", "pip", "install", "--upgrade", "pip", "-q"])

        print("      Installing requirements.txt...")
        run([str(pip), "install", "-r", str(repo / "requirements.txt"), "-q"])
    else:
        print("[1/3] Skipped (--skip-venv).")

    # ── Step 2: spaCy model ───────────────────────────────────────────────────
    if not args.skip_spacy:
        print("[2/3] Downloading spaCy en_core_web_md model...")
        run([str(python), "-m", "spacy", "download", "en_core_web_md"])
    else:
        print("[2/3] Skipped (--skip-spacy).")

    # ── Step 3: render config files ───────────────────────────────────────────
    if not args.skip_config:
        print("[3/3] Rendering config files...")

        src_dir   = args.src_dir or str(repo / "source_docs")
        out_dir   = str(repo / "JSON")
        tesseract = args.tesseract or find_tesseract()

        indexer_cfg = repo / "indexers" / "config.py"
        if indexer_cfg.exists() and not args.force_config:
            print(f"      Skipped (already exists): {indexer_cfg}")
        else:
            indexer_cfg.write_text(
                INDEXER_CONFIG_TEMPLATE.format(
                    src_dir=src_dir, out_dir=out_dir, tesseract_path=tesseract
                ),
                encoding="utf-8",
            )
            print(f"      Written: {indexer_cfg}")

        searches_cfg = repo / "Searches" / "config.ps1"
        if searches_cfg.exists() and not args.force_config:
            print(f"      Skipped (already exists): {searches_cfg}")
        else:
            searches_cfg.write_text(
                SEARCHES_CONFIG_TEMPLATE.format(
                    search_mode=args.search_mode,
                    out_dir=out_dir,
                    jira_source=args.jira_source,
                ),
                encoding="utf-8",
            )
            print(f"      Written: {searches_cfg}")
    else:
        print("[3/3] Skipped (--skip-config).")

    print("\n=== Setup complete ===")
    print(f"Activate venv : {venv}\\Scripts\\Activate.ps1")
    print(f"Run tests     : cd indexers && python tests/test_json_indexer.py")
    print(f"Build index   : cd indexers && python build_index.py\n")


if __name__ == "__main__":
    main()
