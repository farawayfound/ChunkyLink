"""
setup_local.py -- vpoRAG local environment setup for Windows.

Equivalent to running the Ansible Local playbook but runs natively on Windows
without WSL. Ansible requires Unix (fcntl) and cannot run on native Windows Python.

Usage:
    python setup_local.py [options]

Options:
    --repo-root PATH        Path to vpo_rag repo root (default: parent of this script's dir)
    --src-dir PATH          Source documents directory (written to indexers/config.py)
    --tesseract PATH        Tesseract install path (default: auto-detect common locations)
    --search-mode MODE      "Local" or "MCP" for Searches/config.ps1 (default: Local)
    --jira-source SOURCE    "csv", "mysql", or "sql" (default: csv)
    --skip-venv             Skip venv creation and pip install
    --skip-spacy            Skip spaCy model download
    --skip-config           Skip rendering config files
    --force-config          Overwrite existing config files (default: skip if present)
    --python BIN            Python executable to use for venv (default: python)
"""
